    private void showTrends() {
        
        //TrendsLabel.setLocation(TrendsLabel.getLocation().x,TrendsLabel.getLocation().y);
        
        Timer timer = new Timer (100, new ActionListener() { public void actionPerformed(ActionEvent e) {
           
                TrendsLabel.setText(TwitterObj.getTrends());
                //TrendsLabel.setLocation(TrendsLabel.getSize().width-1000,TrendsLabel.getLocation().y);
                if(TrendsLabel.getLocation().x > -3000)
                    TrendsLabel.setLocation(TrendsLabel.getLocation().x-5,TrendsLabel.getLocation().y);
                else
                {
                    TrendsLabel.setLocation(TrendsLabel.getSize().width-1000,TrendsLabel.getLocation().y);
                }
            
        }
        });
        timer.start();
    }
    
